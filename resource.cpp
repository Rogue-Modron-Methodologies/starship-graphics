//
// (¯`'•.¸//(*_*)\\¸.•'´¯`'•.¸//(*_*)\\¸.•'´¯`'•.¸//(*_*)\\¸.•'´¯) 
bool Resource::gainItem(std::string &statusUpdate)
{
	if (qty < rCap)	
	{
		qty++;
		updateTextRect();
		return true;
	}
	else
		statusUpdate = name + " hold is full!";
	return false;
}
