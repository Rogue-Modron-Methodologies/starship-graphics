bool Resource::loseItem(std::string &statusUpdate)
{
	if (qty > 0)		
	{		
		qty--;
		updateTextRect();
		return true;
	}
	else
		statusUpdate = name + " hold is empty!";
	return false;
}

// (¯`'•.¸//(*_*)\\¸.•'´¯`'•.¸//(*_*)\\¸.•'´¯`'•.¸//(*_*)\\¸.•'´¯) 
//  
